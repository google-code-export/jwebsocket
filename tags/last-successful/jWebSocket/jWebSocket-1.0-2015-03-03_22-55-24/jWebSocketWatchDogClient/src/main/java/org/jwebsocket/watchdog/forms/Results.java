    private void initTableTest() {
        //Setting the name of the columns of the Test table
        //testId=1 Test, testResult=false, fatal=false
        String[] columnsnamestest = new String[]{"id", "Result", "Fatal"};
        mModelTest = new DefaultTableModel(columnsnamestest, 0);
        jTable1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        jTable1.setColumnSelectionAllowed(false);
        jTable1.setRowSelectionAllowed(true);
        jTable1.setModel(mModelTest);
        
    }

    private void loadTestTable() {

        initTableTest();
        String lResults = "Success";
        String lFatal = "No";
        for (int i = 0; i < mReport.getReport().size(); i++) {
            if (mReport.getReport().get(i).getTestResult() == false) {
                lResults = "Failed";
            }
            if (mReport.getReport().get(i).isFatal() == true) {
                lFatal = "Yes";
            }
            mModelTest.addRow(new String[]{mReport.getReport().
                        get(i).getTestId().toString(), lResults, lFatal});

            lResults = "Success";
            lFatal = "No";
        }
    }

